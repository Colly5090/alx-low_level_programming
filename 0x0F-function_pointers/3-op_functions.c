#include "3-calc.h"

/**
 * op_add - function sum up values
 * @a: first int value
 * @b: second int value
 * Return: an integer value
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract input values
 * @a: first int value
 * @b: second int value
 * Return: an int value
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiply input values
 * @a: first int value
 * @b: second int value
 * Return: an int value
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides input values
 * @a: first int value
 * @b: second int value
 * Return: an int value
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that return remainder of the division
 * @a: first int value
 * @b: second int value
 * Return: an int value
 */

int op_mod(int a, int b)
{
	return (a % b);
}
